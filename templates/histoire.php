     <h3>Préhistoire</h3>
        
	<p>La région est le lieu d'une implantation humaine ancienne, remontant au néolithique (début du Ve millénaire av. J.-C.). Les traces de plusieurs villages de cette époque ont été découvertes dans la région. Des vestiges du chalcolithique ont été découverts dans le centre-ville.</p>

	<p> principal établissement de la tribu thrace des Serdes se trouvait sur l'emplacement de l'actuelle Sofia ; leur culture semble marquée par une forte influence des Celtes.</p>
 <h3>Antiquité</h3>
 <p>La région est conquise par les Romains, en 29 de l’ère chrétienne, qui font de Serdica la capitale de la province de Dacie. Au iiie siècle, sous Aurélien, les Romains ont bâti d'épaisses murailles autour de la ville de Serdica, capitale de la Dacie intérieure et importante étape sur la route romaine de Naissus (actuelle Niš, Serbie) à Byzantium (actuelle Istanbul, Turquie). En 311, l'empereur romain Galère y signe un édit de tolérance qui met fin à la persécution des chrétiens.</p>
<p>En 441, la ville est prise et pillée par les Huns. Elle est rebâtie par l'empereur byzantin Justinien, sous le nom de Triaditsa.</p>
<h3>Moyen Âge</h3>
<p>En 809, la ville est conquise par les Bulgares et elle reçoit le nom de Sredets. Elle est rapidement considérée comme l'une des plus importantes villes féodales et un prome stratégique lui est assigné sous le premier Empire bulgare.</p>
<p>En 1096, les chevaliers francs du comte de Vermandois et ceux du comte de Toulouse stationnent en cantonnement à Sofia, lors de la première croisade.</p>
<p>Au sud de Sofia, au pied du mont Vitocha, se trouve l'église de Boyana dont les fresques qui remontent au milieu du xiiie siècle sont un des vestiges culturels bulgares et européens les plus importants.</p>
<p>	En 1382, elle est prise par les Ottomans qui en font la capitale de la province de Roumélie.</p>
<h3>Époque moderne</h3>
<p>À l'époque moderne, sous la domination ottomane, la ville est une des résidences du beylerbey de la vaste province de Roumélie, un des plus hauts dignitaires de l'Empire. Cependant, elle décline au xviiie siècle quand les pachas choisissent de résider à Monastir. Elle devient une petite capitale provinciale sans grand rayonnement. La mosquée Bania Bachi, construite en 1567, est un des rares vestiges de cette période.</p>
<p>Adolphe Blanqui, qui visita Sofia en 1841, qualifiera la ville de « sale et infecte », et donne un tableau saisissant des humiliations auxquelles les chrétiens y étaient exposés.</p>
<h3>Époque contemporaine</h3>
<p>En 1879, à la suite de la guerre russo-turque de 1877-1878, Sofia devient la capitale de la principauté de Bulgarie. Elle change alors rapidement de visage, se transformant en métropole occidentale moderne.</p>
<p>Les plans établis en 1881-1882 sont suivis d'une période de construction de bâtiments en briques et de voies de circulation. Plusieurs bâtiments et parcs dans l'actuel centre-ville remontent à cette époque. Le conseil municipal a approuvé en 1900 l'emblème de Sofia et sa devise : « Grandis mais ne vieillis pas ».</p>
<p>À la suite de l'Armistice de Thessalonique du 29 septembre 1918, les troupes françaises stationnent aux portes de Sofia pendant près de deux ans.</p>
<p>En septembre 1944, Sofia est libérée par les troupes de l'Armée rouge commandées par le maréchal Tolboukhine.</p>
<p>Sous le régime communiste (9 septembre 1944 - 10 novembre 1989), Sofia reste le principal centre politique, économique, culturel, scientifique et universitaire de la Bulgarie, dont témoigne l'ensemble architectural Largo.</p>
<p>En 1992, le Gouvernement choisit, en l'honneur de sainte Sophie la Martyre, le 17 septembre comme jour de la ville de Sofia. Le drapeau de la municipalité de Sofia a été consacré le même jour.</p>
