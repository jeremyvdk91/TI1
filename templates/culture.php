    <h3>Patrimoine</h3>
    <h4>La Basilique Sainte-Sophie, qui a donné son nom à la capitale.</h4>
    <p>C'est la plus grande basilique byzantine, construite en dehors de Constantinople au vie siècle, du temps de Justinien. Elle fut transformée en mosquée par les Ottomans puis est redevenue une église orthodoxe à la fin du xixe siècle, après la libération de la Bulgarie du joug ottoman.</p>
    <h4>Église Saint-Georges</h4>
    <p>C'est la plus ancienne église de Sofia dont la construction remonte au ve siècle. La rotonde Saint-Georges date du vie siècle. Elle est située dans une cour entourée de ruines romaines. Elle fut transformée en mosquée sous l’occupation ottomane avant de devenir un musée après la libération de la fin du xixe siècle. On peut y voir trois couches de fresques, la plus ancienne datant du xe siècle.</p>
    <h4>Église de Boyana</h4>
    <p>Inscrite en 1979 sur la liste du patrimoine mondial.</p>
    <h4>Mosquée Bania Bachi</h4>
    <p>C'est l'une des plus anciennes mosquées d'Europe dont la construction remonte au xvie siècle. C'est Mimar Sinan qui en fut l'architecte à l'époque ottomane.
	C'est la seule mosquée de Sofia encore utilisée par les musulmans de la ville.</p>
    <h4>Cathédrale Sainte-Nédélia de Sofia</h4>
    <h4>Cathédrale Saint Alexandre Nevski</h4>
    <p>Construite entre 1882 et 1912 dans le style byzantin, typique des églises russes du xixe siècle, la cathédrale mesure 76 mètres de long et 53 mètres de large et peut contenir jusqu’à 7 000 personnes. Certains des plus grands artistes russes et bulgares de l’époque ont travaillé sur l’intérieur de la cathédrale (5 ailes et 3 autels).
	À l’intérieur, on retrouve : marbres de Sienne et de Carrare dans l’entrée, mosaïques vénitiennes et peintures murales dramatiques comme « le jour du jugement ». Les impressionnants dômes extérieurs furent couverts de feuilles d’or, dons de l'URSS en 1960.</p>
    <h3>Musées</h3>
    <h4>Musée d'art sacré</h4>
    <p>La crypte de la cathédrale Saint Alexandre Nevski abrite la collection d'art orthodoxe de la Galerie nationale des beaux-arts. Elle présente surtout l’art de l’icône. La collection couvre d'objets et d’œuvres d'art allant du ive siècle, date de l'adoption du christianisme comme religion officielle dans l'Empire romain à la Renaissance bulgare des xviiie et xixe siècle.</p>
    <h4>La Galerie nationale des beaux-arts</h4>
    <p>Est le plus grand et le plus riche musée dans le pays. Il offre un panorama de l'art bulgare. Installé dans le bâtiment de l'ancien palais royal à Sofia (classé monument culturel en 1978) construit en 1882 qui abrite également le Musée national d'ethnologie.
	Les collections d'art bulgare moderne, de la libération du pays de la domination ottomane (1878) jusqu’aux années 1990 comptent plus de 30 000 œuvres organisées dans trois grandes expositions : peintures de chevalet, estampes et sculptures.</p>
    <h4>Musée national d'ethnographie</h4>
    <p>Le Musée ethnographique expose des costumes, des instruments de musique et de l’artisanat.</p>
    <h4>Musée national d'archéologie</h4>
    <p>Installé dans une mosquée du xve siècle, ce musée est géré par l’Académie des sciences bulgare. Les collections conservent des objets allant de la Préhistoire au Moyen Âge notamment dans la salle des trésors, située au premier étage, contenu de six tombeaux de guerre en provenance du nécropole de Trebenishte, en Macédoine du Nord, et les trois trésors thraces, d'importance mondiale.</p>
    <h4>Musée national d'histoire militaire</h4>
    <p>Le musée national d’histoire militaire expose des armes, des uniformes et des drapeaux. De nombreux chars, des avions et autres moyens de transport datant du début du xxe siècle sont exposés dans le jardin du musée.</p>
    <h4>Musée national d'histoire</h4>
    <p>Proche de l’église de Boyana, il a été installé en 2000, hors de la ville à la Résidence Boyana (la résidence officielle du dirigeant communiste Todor Jivkov). Le musée abrite une grande quantité d’objets, allant des premiers temps du peuplement en Bulgarie à l'époque contemporaine. On y trouve des bijoux thraces et des masques funéraires de la vallée des Rois, des effets personnels du révolutionnaire Vasil Levski ainsi que la constitution signée en 1879.</p>
    <h4>Musée Ivan Vazaov</h4>
    <h4>L'Opéra national</h4>
    <p>Ivan Vazov est considéré comme l’écrivain bulgare le plus populaire qui vécut au tournant du xxe siècle. Ses œuvres comprennent des romans, de la littérature pour enfants, des poèmes et des pièces de théâtre. Presque tous les Bulgares peuvent réciter de mémoire son poème Az sam Bulgarche (« Je suis un petit bulgare »). Dans sa maison, sont exposés ses objets personnels, y compris son chien naturalisé.</p>
    <h4>Musée national d'histoire naturelle</h4>
    <p>Les collections sont présentées dans seize salles sur quatre niveaux. Parmi pièces les plus précieuses figurent la Conure de Caroline (ou Conure à tête jaune) (Conuropsis carolinensis), espèce disparue au début du xxe siècle, et des pierres lunaires. Sont exposées des collections de minéraux, d'animaux, des insectes... Dans une autre salle est présenté le processus de formation des métaux.</p>
